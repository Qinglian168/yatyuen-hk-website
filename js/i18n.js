// Language management
let currentLang = 'zh-TW';

function getLang() {
    return currentLang;
}

function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('yy_lang', lang);
    applyTranslations();
    // Re-render product-related content if on products page
    if (typeof renderProducts === 'function') {
        renderProducts();
    }
    // Update active nav
    updateActiveNav();
}

function t(key) {
    const lang = currentLang;
    return i18nData[lang] && i18nData[lang][key] ? i18nData[lang][key] : key;
}

function applyTranslations() {
    // Update title if it has data-i18n
    var titleEl = document.querySelector('title[data-i18n]');
    if (titleEl) {
        var key = titleEl.getAttribute('data-i18n');
        var translated = t(key);
        if (translated !== key) {
            titleEl.textContent = translated;
        }
    }

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translated = t(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = translated;
        } else if (el.tagName === 'TITLE') {
            // already handled above
        } else {
            el.textContent = translated;
        }
    });

    // Update product select options if on contact page
    if (typeof window.populateProductSelect === 'function') {
        window.populateProductSelect();
    }

    // Update hero scene image based on language
    var heroSceneImg = document.getElementById('heroSceneImg');
    if (heroSceneImg) {
        heroSceneImg.src = 'assets/hero-scenes/scene-' + (currentLang === 'zh-TW' ? 'zh' : 'en') + '.png';
    }

    // Update about page visual image based on language
    var aboutVisualImg = document.getElementById('aboutVisualImg');
    if (aboutVisualImg) {
        if (currentLang === 'zh-TW') {
            aboutVisualImg.src = 'assets/hero-scenes/company-intro-zh.png';
            aboutVisualImg.alt = '逸源（香港）公司介紹';
        } else {
            aboutVisualImg.src = 'assets/hero-scenes/company-intro-en.png';
            aboutVisualImg.alt = 'Company Introduction - YAT YUEN HK';
        }
    }
}

function initLang() {
    const saved = localStorage.getItem('yy_lang');
    if (saved) {
        currentLang = saved;
    }
    applyTranslations();
}
